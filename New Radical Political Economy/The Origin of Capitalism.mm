    <p>
      Polanyi is remembered today as the originator of substantivism, a cultural approach to economics, which emphasized the way economies are embedded in society and culture. This worked against mainstream economics but was popular in anthropology and political science. His book The Great Transformation also became a model for historical sociology. His theories eventually became the foundation for the economic democracy movement.
    </p>
    <p>
      Substantivism is associated with Karl Polanyi. Polanyi saw two dimensions to the term 'economics'. In the first it refers to the rational decision-making and rational action concerning the use of scare resources. The second substantive dimension in economics refers to the study of how humans make a living from their social and natural environment. A the macro level then, a societies economic activity demonstrates how reason and scarcity are environmental and material.
    </p>
