<node CREATED="1196295016488" ID="Freemind_Link_1511169877" MODIFIED="1196295170618" TEXT="Nietzsche issues a deninal of teleological notions of human life">
<richcontent TYPE="NOTE"><html>
  <head>
    
  </head>
  <body>
    <p>
      <b>Teleology (Greek: telos: end, purpose) </b>
    </p>
    <p>
      is the philosophical study of design, purpose, directive principle, or finality in nature or human creations.
    </p>
    <p>
      It is traditionally contrasted with philosophical naturalism, which views nature as lacking design or purpose.
    </p>
    <p>
      In opposition to this, teleology holds there is <b>a final cause or purpose inherent in all beings</b>. There are two types of such causes, intrinsic finality and extrinsic finality.
    </p>
    <p>
      Historically, teleology may be identified with Aristotelianism and the scholastic tradition in philosophy, again, made central to speculative philosophy by Hegel. The rationale of teleology was explored in detail by Immanuel Kant in his Critique of Judgement.
    </p>
  </body>
</html>
